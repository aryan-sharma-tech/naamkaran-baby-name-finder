function setGender(g) {
  currentGender = g;
  displayNames();
}

function setLetter(l) {
  currentLetter = l;
  displayNames();
}

function displayNames() {
  const list = document.getElementById("nameList");
  list.innerHTML = "";

  const search = document.getElementById("searchInput").value.toLowerCase();
  let finalList = [];

  function collect(group) {
    for (let cat in group) {
      if (currentCategory === "" || cat === currentCategory) {
        group[cat].forEach(name => finalList.push(name));
      }
    }
  }

  if (currentGender === "all" || currentGender === "boy") collect(babyNames.boys);
  if (currentGender === "all" || currentGender === "girl") collect(babyNames.girls);

  finalList
    .filter(n =>
      (currentLetter === "" || n.startsWith(currentLetter)) &&
      n.toLowerCase().includes(search)
    )
    .forEach(n => {
      const div = document.createElement("div");
      div.className = "name-card";
      div.innerText = n;
      list.appendChild(div);
    });
}

